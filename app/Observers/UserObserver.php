     * @return void
     */
    public function created(User $user)
    {
        if(Invitation::where('email', $user->email)->whereNull('accepted_at')->count())
        {
            Invitation::where('email', $user->email)->update([
                'accepted_at' => Carbon::now(),
            ]);
        }
    }

